// Regenerates src/data/allcodes.ts from bennetyee.org's area code listing.
//   node scripts/scrape-allcodes.mjs [path-to-saved-area.html]
// With no argument, fetches the live page.
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const SOURCE_URL = 'https://www.bennetyee.org/ucsd-pages/area.html';

const html = process.argv[2]
  ? readFileSync(process.argv[2], 'utf8')
  : await (await fetch(SOURCE_URL)).text();

const rowRe =
  /<tr><td align=center><a name="([^"]+)">[^<]*<\/a><\/td><td align=center>([^<]*)<\/td><td align=center>([^<]*)<\/td><td>([\s\S]*?)<\/td><\/tr>/g;

// Manual corrections where the source's status is wrong. Only overlays
// whose suspension actually HELD go here (278 on 734, 764 on 650 — never
// entered service); 341/737/747 also say "suspended" but later went live.
// 927 was set aside for Orlando-area cellular in 90s relief planning and
// never entered service either.
const STATUS_FIXES = { '278': 'suspended', '764': 'suspended', '927': 'suspended' };

function status(desc) {
  const d = desc.toLowerCase();
  if (d.includes('no longer in use')) return 'retired';
  if (d.includes('assigned but not in use')) return 'assigned';
  if (d.includes('proposed, may not be in use')) return 'proposed';
  if (d.includes('pending')) return 'assigned'; // e.g. 957 "pending; region unknown" — never in service
  if (d.startsWith('reserved')) return 'reserved';
  if (d === 'unassigned') return 'unassigned';
  return 'active';
}

const entries = [];
let m;
while ((m = rowRe.exec(html))) {
  const code = m[1].trim();
  if (!/^\d{3}$/.test(code)) continue; // skip the "52 55" Mexico row
  const description = m[4]
    .replace(/<[^>]+>/g, '')
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/\s+/g, ' ')
    .trim();
  entries.push({
    code,
    region: m[2].trim(),
    utcOffset: m[3].trim(),
    description,
    status: STATUS_FIXES[code] ?? status(description),
  });
}

entries.sort((a, b) => a.code.localeCompare(b.code));

let out = `// Generated by scripts/scrape-allcodes.mjs — do not edit by hand.
// Source: ${SOURCE_URL} (a community-maintained list; some entries are stale,
// e.g. codes marked 'proposed'/'assigned' have long since gone live).

export type AreaCodeStatus =
  | 'active'
  | 'assigned' // assigned but listed as not yet in use
  | 'proposed'
  | 'reserved' // special applications (211, 311, 555, 911, ...)
  | 'retired'
  | 'suspended' // overlay canceled before entering service (see STATUS_FIXES)
  | 'unassigned';

export interface AreaCodeInfo {
  code: string;
  /** US state / Canadian province / territory abbreviation, or '--' for non-geographic. */
  region: string;
  /** Raw UTC offset text from the source, e.g. '-5', '-6/-7*', '--'. */
  utcOffset: string;
  description: string;
  status: AreaCodeStatus;
}

export const ALL_CODES: Record<string, AreaCodeInfo> = {
`;
for (const e of entries) {
  out += `  '${e.code}': ${JSON.stringify(e)},\n`;
}
out += '};\n';

const dest = join(dirname(fileURLToPath(import.meta.url)), '../src/data/allcodes.ts');
writeFileSync(dest, out);
console.log(`wrote ${entries.length} codes to ${dest}`);
