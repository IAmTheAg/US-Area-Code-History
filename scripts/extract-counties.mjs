// Regenerates src/data/counties/*.ts and src/data/states.ts from the
// repo-root usa_counties.svg (Wikimedia "Usa_counties_large.svg", U.S.
// Census Bureau shapes, CC0).
//
//   node scripts/extract-counties.mjs        (run from app/)
//
// Outputs:
//  - src/data/counties/<XX>.ts  one per state + DC: county name -> path d,
//    plus a CountyName union type for typo-safe region files.
//  - src/data/counties/index.ts aggregator (COUNTIES_BY_STATE).
//  - src/data/states.ts         state outlines = the union of each state's
//    counties, dilated by a hair so counties that almost touch merge (the
//    source shapes were simplified independently and leave hairline gaps).
//    Sliver holes are dropped; real lakes (Pontchartrain, Okeechobee...)
//    and islands survive.
//
// IMPORTANT: scripts/port-regions.mjs reads the OLD states.ts to learn the
// old per-state bounding boxes. Port first, extract second.

import fs from 'node:fs';
import path from 'node:path';
import polygonClipping from 'polygon-clipping';
import {
  REPO_ROOT,
  ID_TO_NAME,
  parseCountiesSvg,
  ringsToPath,
  ringArea,
  bboxOfRings,
} from './lib/svgmap.mjs';

const DATA_DIR = path.join(REPO_ROOT, 'app/src/data');
const COUNTIES_DIR = path.join(DATA_DIR, 'counties');

const DILATE_R = 0.15; // map units; merges near-touching counties in the union
const MIN_OUTER_AREA = 0.25; // drop dust rings (subpixel at map scale)
const MIN_HOLE_AREA = 2.0; // drop gap slivers, keep real lakes
const SIMPLIFY_EPS = 0.12; // RDP tolerance for outline rings

const snap = (v) => Math.round(v * 500) / 500;

/** Move each vertex outward along the averaged normal of its two edges. */
function dilate(ring, r) {
  const n = ring.length;
  const sign = ringArea(ring) > 0 ? 1 : -1;
  return ring.map((p, i) => {
    const a = ring[(i - 1 + n) % n];
    const b = ring[(i + 1) % n];
    let nx = 0;
    let ny = 0;
    for (const [p1, p2] of [[a, p], [p, b]]) {
      const dx = p2[0] - p1[0];
      const dy = p2[1] - p1[1];
      const len = Math.hypot(dx, dy) || 1;
      nx += (dy / len) * sign;
      ny += (-dx / len) * sign;
    }
    const len = Math.hypot(nx, ny) || 1;
    return [snap(p[0] + (nx / len) * r), snap(p[1] + (ny / len) * r)];
  });
}

/** Ramer–Douglas–Peucker on a closed ring. */
function simplifyRing(ring, eps) {
  if (ring.length <= 4) return ring;
  const keep = new Array(ring.length).fill(false);
  keep[0] = true;
  keep[ring.length - 1] = true;
  const stack = [[0, ring.length - 1]];
  while (stack.length) {
    const [lo, hi] = stack.pop();
    const [ax, ay] = ring[lo];
    const [bx, by] = ring[hi];
    const dx = bx - ax;
    const dy = by - ay;
    const len = Math.hypot(dx, dy) || 1;
    let worst = -1;
    let worstD = eps;
    for (let i = lo + 1; i < hi; i++) {
      const d = Math.abs((ring[i][0] - ax) * dy - (ring[i][1] - ay) * dx) / len;
      if (d > worstD) {
        worstD = d;
        worst = i;
      }
    }
    if (worst !== -1) {
      keep[worst] = true;
      stack.push([lo, worst], [worst, hi]);
    }
  }
  const out = ring.filter((_, i) => keep[i]);
  return out.length >= 3 ? out : ring;
}

function stateOutline(counties) {
  const geoms = [];
  for (const c of counties) {
    for (const ring of c.rings) {
      const d = dilate(ring, DILATE_R);
      geoms.push([[...d, d[0]]]);
    }
  }
  const union = polygonClipping.union(...geoms);
  const rings = [];
  let droppedHoles = 0;
  const keptHoles = [];
  for (const poly of union) {
    const [outer, ...holes] = poly;
    if (Math.abs(ringArea(outer)) < MIN_OUTER_AREA) continue;
    rings.push(simplifyRing(outer.slice(0, -1), SIMPLIFY_EPS));
    for (const hole of holes) {
      const a = Math.abs(ringArea(hole));
      if (a < MIN_HOLE_AREA) {
        droppedHoles++;
        continue;
      }
      keptHoles.push(a);
      rings.push(simplifyRing(hole.slice(0, -1), SIMPLIFY_EPS));
    }
  }
  return { rings, droppedHoles, keptHoles };
}

const { viewBox, states } = parseCountiesSvg();
const ids = [...states.keys()].sort();

fs.mkdirSync(COUNTIES_DIR, { recursive: true });

const GENERATED_NOTE = `// Generated by scripts/extract-counties.mjs from usa_counties.svg
// (Wikimedia "Usa_counties_large.svg", U.S. Census Bureau shapes, CC0).
// DO NOT EDIT — rerun the script instead.`;

// --- per-state county files + aggregator ------------------------------------
let totalCounties = 0;
for (const id of ids) {
  const counties = states.get(id);
  totalCounties += counties.length;
  const entries = counties
    .map((c) => `  ${JSON.stringify(c.name)}: '${ringsToPath(c.rings)}',`)
    .join('\n');
  const src = `${GENERATED_NOTE}

/** ${ID_TO_NAME[id]}: county name -> rough shape path (map coordinates). */
export const ${id}_COUNTIES = {
${entries}
} as const;

export type ${id}CountyName = keyof typeof ${id}_COUNTIES;
`;
  fs.writeFileSync(path.join(COUNTIES_DIR, `${id}.ts`), src);
}

const indexSrc = `${GENERATED_NOTE}

${ids.map((id) => `import { ${id}_COUNTIES } from './${id}';`).join('\n')}

/** stateId -> county name -> path d. */
export const COUNTIES_BY_STATE: Record<string, Record<string, string>> = {
${ids.map((id) => `  ${id}: ${id}_COUNTIES,`).join('\n')}
};
`;
fs.writeFileSync(path.join(COUNTIES_DIR, 'index.ts'), indexSrc);

// --- states.ts with union outlines ------------------------------------------
const stateEntries = [];
for (const id of ids) {
  const counties = states.get(id);
  const { rings, keptHoles } = stateOutline(counties);
  const d = ringsToPath(rings);
  const b = bboxOfRings([rings]);
  stateEntries.push(`  { id: ${JSON.stringify(id)}, name: ${JSON.stringify(ID_TO_NAME[id])}, d: "${d}" },`);
  console.log(
    id,
    `${String(counties.length).padStart(3)} counties,`,
    `outline: ${rings.length} ring(s), ${rings.reduce((n, r) => n + r.length, 0)} pts,`,
    `bbox x ${b.minX.toFixed(1)}-${b.maxX.toFixed(1)} y ${b.minY.toFixed(1)}-${b.maxY.toFixed(1)}`,
    keptHoles.length ? `| lakes kept: ${keptHoles.map((a) => a.toFixed(1)).join(', ')}` : '',
  );
}

const statesSrc = `${GENERATED_NOTE}
//
// Each state's outline is the dilated union of its county shapes, so it
// matches the county layer everywhere (coastlines included). viewBox:
// 0 0 ${viewBox.w} ${viewBox.h}.

export interface StateShape {
  id: string;
  name: string;
  d: string;
}

export const MAP_VIEWBOX = '0 0 ${viewBox.w} ${viewBox.h}';

export const STATES: StateShape[] = [
${stateEntries.join('\n')}
];
`;
fs.writeFileSync(path.join(DATA_DIR, 'states.ts'), statesSrc);

// --- reference output for hand-tuned constants -------------------------------
const dc = bboxOfRings(states.get('DC').map((c) => c.rings));
console.log(`\n${totalCounties} counties total.`);
console.log(
  `DC center (for the UsMap DC_DOT): ${((dc.minX + dc.maxX) / 2).toFixed(1)}, ${((dc.minY + dc.maxY) / 2).toFixed(1)}`,
);
